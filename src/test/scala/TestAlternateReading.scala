package org.homermultitext.edmodel
import org.scalatest.FlatSpec

class AlternateReadingSpec extends FlatSpec {
  "An alternate reading" should "have a vector of 0 or more readings" in pending
  it should "have a category" in pending
  it should "have no readings when its category is Original" in pending
  it should "have 1 or more readings when its category is not Original" in pending
}
